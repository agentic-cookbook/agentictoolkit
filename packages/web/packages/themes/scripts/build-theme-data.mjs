#!/usr/bin/env node
// Reads packages/themes/src/styles/*.css and emits src/theme-data.ts
// with string-literal exports keyed by file basename. Runs as prebuild.
import { readdir, readFile, writeFile } from 'node:fs/promises'
import { join, resolve } from 'node:path'

const STYLES_DIR = resolve('src/styles')
const OUT = resolve('src/theme-data.ts')

function camelize(name) {
  return name.replace(/-([a-z])/g, (_, c) => c.toUpperCase())
}

const files = (await readdir(STYLES_DIR)).filter((f) => f.endsWith('.css')).sort()

const lines = ['// AUTO-GENERATED by scripts/build-theme-data.mjs — do not edit.', '']
for (const file of files) {
  const css = await readFile(join(STYLES_DIR, file), 'utf8')
  const varName = `${camelize(file.replace(/\.css$/, ''))}Css`
  lines.push(`export const ${varName} = ${JSON.stringify(css)}`)
}
lines.push('')

await writeFile(OUT, lines.join('\n'), 'utf8')
console.log(`build-theme-data: ${files.length} theme${files.length === 1 ? '' : 's'} → src/theme-data.ts`)
